"use client";

import { useEffect, useState } from "react";
import { useRouter } from "next/navigation";
import { 
  Trophy, 
  CheckCircle, 
  XCircle, 
  FileText, 
  Download,
  Home,
  BarChart3,
  Target,
  Share2
} from "lucide-react";
import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";

type ResultData = {
  total: number;
  correct: number;
  wrong: number;
  score: number;
  percentage?: number;
  correctAnswers?: Record<string, number>;
  userAnswers?: Record<string, number>;
  questions?: Array<{
    id: string;
    question: string;
    correctAnswer: number;
    userAnswer: number;
    options: string[];
  }>;
  duration?: number;
  faculty?: string;
  subject?: string;
  date?: string;
};

export default function ResultPage() {
  const router = useRouter();
  const [result, setResult] = useState<ResultData | null>(null);
  const [loading, setLoading] = useState(true);
  const [downloading, setDownloading] = useState(false);

  useEffect(() => {
    try {
      const data = JSON.parse(localStorage.getItem("mocktest_result") || "null");
      if (data) {
        // Calculate percentage if not present
        if (data.total && data.correct !== undefined) {
          data.percentage = (data.correct / data.total) * 100;
        }
        // Handle score if it's an object (from the new structure)
        if (data.score && typeof data.score === 'object') {
          data.total = data.score.total;
          data.correct = data.score.obtained;
          data.wrong = data.score.total - data.score.obtained;
          data.score = data.score.percentage;
          data.percentage = data.score;
        }
        // Add timestamp if not present
        if (!data.date) {
          data.date = new Date().toLocaleString();
        }
        setResult(data);
      }
    } catch (err) {
      console.error("Failed to load result", err);
    } finally {
      setLoading(false);
    }
  }, []);

  const downloadPDF = () => {
    if (!result) return;
    
    setDownloading(true);
    
    try {
      const doc = new jsPDF();
      
      // Header
      doc.setFontSize(20);
      doc.setTextColor(59, 130, 246);
      doc.text("Mock Test Result", 105, 20, { align: "center" });
      
      // Date
      doc.setFontSize(10);
      doc.setTextColor(100, 100, 100);
      doc.text(`Generated: ${result.date || new Date().toLocaleString()}`, 105, 30, { align: "center" });
      
      // Get safe percentage value
      const safePercentage = typeof result.percentage === 'number' ? result.percentage : 
                            (typeof result.score === 'number' ? result.score : 0);
      
      // Score Card
      doc.setFillColor(59, 130, 246);
      doc.rect(20, 40, 170, 40, "F");
      doc.setTextColor(255, 255, 255);
      doc.setFontSize(16);
      doc.text(`Score: ${safePercentage.toFixed(1)}%`, 105, 55, { align: "center" });
      doc.setFontSize(12);
      doc.text(`Result: ${safePercentage >= 60 ? "PASSED" : "NEEDS IMPROVEMENT"}`, 105, 68, { align: "center" });
      
      // Statistics Table
      doc.setTextColor(0, 0, 0);
      doc.setFontSize(14);
      doc.text("Performance Summary", 20, 95);
      
      const statsData = [
        ["Total Questions", result.total?.toString() || "0"],
        ["Correct Answers", result.correct?.toString() || "0"],
        ["Wrong Answers", result.wrong?.toString() || "0"],
        ["Accuracy", `${safePercentage.toFixed(1)}%`],
        ["Status", safePercentage >= 60 ? "PASSED" : "NEEDS IMPROVEMENT"],
      ];
      
      autoTable(doc, {
        startY: 100,
        head: [["Metric", "Value"]],
        body: statsData,
        theme: "striped",
        headStyles: { fillColor: [59, 130, 246], textColor: [255, 255, 255] },
        styles: { fontSize: 10 },
        margin: { left: 20 },
      });
      
      // Question Analysis
      if (result.questions && result.questions.length > 0) {
        doc.addPage();
        doc.setFontSize(14);
        doc.text("Detailed Question Analysis", 20, 20);
        
        const qData = result.questions.map((q, idx) => [
          (idx + 1).toString(),
          q.question.substring(0, 50) + (q.question.length > 50 ? "..." : ""),
          q.userAnswer === q.correctAnswer ? "✓ Correct" : "✗ Incorrect",
        ]);
        
        autoTable(doc, {
          startY: 30,
          head: [["#", "Question", "Result"]],
          body: qData,
          theme: "striped",
          headStyles: { fillColor: [59, 130, 246], textColor: [255, 255, 255] },
          styles: { fontSize: 8 },
          margin: { left: 20 },
        });
      }
      
      // Footer
      const pageCount = doc.getNumberOfPages();
      for (let i = 1; i <= pageCount; i++) {
        doc.setPage(i);
        doc.setFontSize(8);
        doc.setTextColor(150, 150, 150);
        doc.text("Generated by Mock Test Platform", 105, doc.internal.pageSize.height - 10, { align: "center" });
      }
      
      doc.save(`mock-test-result-${Date.now()}.pdf`);
    } catch (err) {
      console.error("PDF generation failed", err);
      alert("Failed to generate PDF. Please try again.");
    } finally {
      setDownloading(false);
    }
  };

  const getScoreColor = (score: number) => {
    if (score >= 80) return "text-green-600";
    if (score >= 60) return "text-blue-600";
    if (score >= 40) return "text-yellow-600";
    return "text-red-600";
  };

  const getScoreMessage = (percentage: number) => {
    if (percentage >= 80) return "Excellent! Outstanding performance! 🎉";
    if (percentage >= 60) return "Good job! Keep improving! 👏";
    if (percentage >= 40) return "Nice attempt! Practice more! 💪";
    return "Keep practicing! You'll do better next time! 📚";
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-white">
        <div className="max-w-2xl mx-auto px-4 py-12">
          <div className="flex flex-col items-center justify-center">
            <div className="w-8 h-8 border-2 border-blue-500 border-t-transparent rounded-full animate-spin mb-3" />
            <p className="text-sm text-slate-500">Loading result...</p>
          </div>
        </div>
      </div>
    );
  }

  if (!result) {
    return (
      <div className="min-h-screen bg-white">
        <div className="max-w-2xl mx-auto px-4 py-12">
          <div className="bg-yellow-50 border border-yellow-200 rounded-lg p-6 text-center">
            <FileText className="w-10 h-10 text-yellow-600 mx-auto mb-3" />
            <p className="text-yellow-800 font-medium mb-2">No Result Found</p>
            <p className="text-yellow-600 text-sm mb-4">Please complete a mock test first</p>
            <button
              onClick={() => router.push("/dashboard/mocktest")}
              className="px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors text-sm"
            >
              Go to Mock Tests
            </button>
          </div>
        </div>
      </div>
    );
  }

  // Safely extract and validate percentage
  let percentage: number;
  if (typeof result.percentage === 'number' && !isNaN(result.percentage)) {
    percentage = result.percentage;
  } else if (typeof result.score === 'number' && !isNaN(result.score)) {
    percentage = result.score;
  } else if (result.total && result.correct !== undefined) {
    percentage = (result.correct / result.total) * 100;
  } else {
    percentage = 0;
  }
  
  // Ensure percentage is a valid number
  const safePercentage = isNaN(percentage) ? 0 : percentage;
  const scoreColor = getScoreColor(safePercentage);
  const scoreMessage = getScoreMessage(safePercentage);
  
  // Safely get other values
  const totalQuestions = result.total || 0;
  const correctAnswers = result.correct || 0;
  const wrongAnswers = result.wrong || (totalQuestions - correctAnswers);
  const displayScore = typeof result.score === 'number' ? result.score : safePercentage;

  return (
    <div className="min-h-screen bg-gradient-to-br from-slate-50 to-blue-50">
      <div className="max-w-4xl mx-auto px-4 py-8">
        {/* Header */}
        <div className="text-center mb-8">
          <div className="inline-flex items-center justify-center p-4 bg-gradient-to-br from-yellow-500 to-orange-500 rounded-full shadow-lg mb-4">
            <Trophy className="w-8 h-8 text-white" />
          </div>
          <h1 className="text-3xl md:text-4xl font-bold text-slate-900 mb-2">
            Test Complete!
          </h1>
          <p className="text-slate-600">
            Here's your performance summary
          </p>
        </div>

        {/* Score Card */}
        <div className="bg-white rounded-2xl shadow-lg border border-slate-200 p-6 mb-6">
          <div className="text-center">
            <div className={`text-6xl font-bold ${scoreColor} mb-3`}>
              {safePercentage.toFixed(1)}%
            </div>
            <div className={`text-lg font-semibold ${scoreColor} mb-2`}>
              Score: {displayScore} / {totalQuestions}
            </div>
            <p className="text-slate-600 text-sm">
              {scoreMessage}
            </p>
          </div>

          {/* Stats Grid */}
          <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mt-6 pt-6 border-t border-slate-100">
            <div className="text-center">
              <div className="inline-flex items-center justify-center w-10 h-10 bg-blue-100 rounded-full mb-2">
                <FileText className="w-5 h-5 text-blue-600" />
              </div>
              <div className="text-2xl font-bold text-slate-900">{totalQuestions}</div>
              <div className="text-xs text-slate-500">Total Questions</div>
            </div>
            <div className="text-center">
              <div className="inline-flex items-center justify-center w-10 h-10 bg-green-100 rounded-full mb-2">
                <CheckCircle className="w-5 h-5 text-green-600" />
              </div>
              <div className="text-2xl font-bold text-green-600">{correctAnswers}</div>
              <div className="text-xs text-slate-500">Correct</div>
            </div>
            <div className="text-center">
              <div className="inline-flex items-center justify-center w-10 h-10 bg-red-100 rounded-full mb-2">
                <XCircle className="w-5 h-5 text-red-600" />
              </div>
              <div className="text-2xl font-bold text-red-600">{wrongAnswers}</div>
              <div className="text-xs text-slate-500">Wrong</div>
            </div>
            <div className="text-center">
              <div className="inline-flex items-center justify-center w-10 h-10 bg-purple-100 rounded-full mb-2">
                <Target className="w-5 h-5 text-purple-600" />
              </div>
              <div className="text-2xl font-bold text-purple-600">
                {safePercentage.toFixed(0)}%
              </div>
              <div className="text-xs text-slate-500">Accuracy</div>
            </div>
          </div>
        </div>

        {/* Detailed Analysis (if available) */}
        {result.questions && result.questions.length > 0 && (
          <div className="bg-white rounded-2xl shadow-lg border border-slate-200 p-6 mb-6">
            <h2 className="text-lg font-semibold text-slate-900 mb-4 flex items-center gap-2">
              <BarChart3 className="w-5 h-5 text-blue-600" />
              Detailed Analysis
            </h2>
            <div className="space-y-3 max-h-96 overflow-y-auto">
              {result.questions.map((q, idx) => (
                <div
                  key={idx}
                  className={`p-3 rounded-lg border ${
                    q.userAnswer === q.correctAnswer
                      ? "bg-green-50 border-green-200"
                      : "bg-red-50 border-red-200"
                  }`}
                >
                  <div className="flex items-start justify-between gap-2">
                    <div className="flex-1">
                      <div className="text-sm font-medium text-slate-900 mb-1">
                        Q{idx + 1}. {q.question}
                      </div>
                      <div className="text-xs text-slate-600">
                        Your answer: Option {String.fromCharCode(65 + (q.userAnswer || 0))}
                        {q.userAnswer !== q.correctAnswer && (
                          <span className="text-green-600 ml-2">
                            (Correct: Option {String.fromCharCode(65 + q.correctAnswer)})
                          </span>
                        )}
                      </div>
                    </div>
                    {q.userAnswer === q.correctAnswer ? (
                      <CheckCircle className="w-5 h-5 text-green-500 flex-shrink-0" />
                    ) : (
                      <XCircle className="w-5 h-5 text-red-500 flex-shrink-0" />
                    )}
                  </div>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Action Buttons */}
        <div className="flex flex-wrap gap-3 justify-center">
          <button
            onClick={() => router.push("/dashboard/mocktest")}
            className="flex items-center gap-2 px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors"
          >
            <Home className="w-4 h-4" />
            Back to Tests
          </button>
          
          <button
            onClick={downloadPDF}
            disabled={downloading}
            className="flex items-center gap-2 px-6 py-3 border-2 border-blue-600 text-blue-600 rounded-lg hover:bg-blue-50 transition-colors disabled:opacity-50"
          >
            {downloading ? (
              <>
                <div className="w-4 h-4 border-2 border-blue-600 border-t-transparent rounded-full animate-spin" />
                Generating...
              </>
            ) : (
              <>
                <Download className="w-4 h-4" />
                Download PDF
              </>
            )}
          </button>

          <button
            onClick={() => window.print()}
            className="flex items-center gap-2 px-6 py-3 border border-slate-300 text-slate-700 rounded-lg hover:bg-slate-50 transition-colors"
          >
            <Share2 className="w-4 h-4" />
            Print Result
          </button>
        </div>

        {/* Share Message */}
        <div className="text-center mt-6 text-xs text-slate-400">
          Great effort! Keep practicing to improve your score.
        </div>
      </div>
    </div>
  );
}